def add(x, y):
  answer = x + y
  return answer

def subtract(x, y):
  answer = x - y
  return answer

def multiply(x, y):
  answer = x * y
  return answer

def divide(x, y):
  answer = x // y
  return answer

def exp(x, y):
  answer = x ** y
  return answer

print(add(3, 4))          # Same thing as print(7)
print(multiply(1, 5))          # Same thing as print(5)
print(subtract(5, 3))          # Same thing as print(2)
print(divide(6, 3))          # Same thing as print(2)
print(exp(3, 4))          # Same thing as print(81)